import UIKit

extension UIColor {
  static func sea_primaryTextColor() -> UIColor {
    return UIColor.whiteColor()
  }
  static func sea_primaryBackgroundColor() -> UIColor {
    return UIColor(red: 0.0/255.0, green: 114.0/255.0, blue: 187.0/255.0, alpha: 1.0)
  }
}
